for (const lang in translations) {
    const langKey = `${lang}: {`;
    let langIndex = content.indexOf(`    ${langKey}`);
    if (langIndex === -1) {
        langIndex = content.indexOf(`\n    ${lang}: {`);
        if (langIndex === -1) continue;
    }
    
    const activityStart = content.indexOf("'menu.activity': 'Activity log'", langIndex);
    if (activityStart === -1) continue;
    
    const nextLangIndex = content.indexOf('},    ', activityStart);
    if (nextLangIndex === -1) continue;
    
    let blockPart = content.substring(activityStart, nextLangIndex);
    
    for (const key in translations[lang]) {
        const regex = new RegExp(`'${key.replace('.', '\\.')}':\\s*'[^']*'`, 'g');
        blockPart = blockPart.replace(regex, `'${key}': '${translations[lang][key].replace(/'/g, "\\'")}'`);
    }
    
    content = content.substring(0, activityStart) + blockPart + content.substring(nextLangIndex);
}

